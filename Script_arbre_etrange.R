## Histoire

Il était une fois, dans une forêt lointaine, un arbre étrange...
De ses branches pendaient des milliers de petites feuilles dont émanait une odeur envoûtante. 
Quand on y prêtait attention, ces feuilles ressemblaient étrangement à de délicats petits sachets de tisane. 
Chacun pouvait se servir après le repas, ou avant d'aller dormir.