## Histoire

Il était une fois, dans une forêt lointaine, un arbre, somme toute étrange...
De ses branches pendaient des milliers de petites feuilles surprenantes dont émanait une odeur envoûtante. 
Quand on y prêtait vraiment attention, ces drôles de feuilles ressemblaient étrangement à de délicats petits sachets de tisane. 
Les sachets murmuraient ces paroles : " infuse moiiii... "
Chacun pouvait se servir à sa guise après le repas, ou avant d'aller dormir paisiblement.